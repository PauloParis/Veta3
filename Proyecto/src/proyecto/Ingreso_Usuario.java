    private void btn_IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IngresarActionPerformed
        String SQL = "SELECT * FROM usuarios";
        
        try {
            PreparedStatement ingresar = conect.prepareStatement(SQL);
            ResultSet rs = ingresar.executeQuery();
            
            while(rs.next()){
                if(txt_Ingreso_Usuario.getText().contentEquals(rs.getString("USU_NOMBRE")) && pass_Ingreso_Password.getText().contentEquals(rs.getString("USU_CLAVE"))){
                    Ventas Venta_cliente = new Ventas();
                    Venta_cliente.setVisible(true);
                    this.dispose();
                }
            }
            
            
        } 
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Usuario o contraseña no coinciden");
        }
        
        
    }//GEN-LAST:event_btn_IngresarActionPerformed

